    String filePath;
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();
        filePath = f.getAbsolutePath();

        try {
            BufferedReader reader = new BufferedReader(new FileReader(filePath));
            String line;
            while((line=reader.readLine()) != null){
                textArea1.append(line+"\n\n");
            }
            reader.close();
        } catch(IOException e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = jTextField1.getText();
        String artist = jTextField2.getText();
        String title = jTextField3.getText();
        String size = jTextField6.getText();
        String duration = jTextField5.getText();
        String author = jTextField7.getText();
        String numberOfPages = jTextField8.getText();
        String director = jTextField9.getText();
        
        MusicCD music1 = new MusicCD("CD",name,artist,title,size,duration);
        VideoDVD video1 = new VideoDVD("DVD", name,director,size,duration);
        EBook ebook1 = new EBook("EBook", name,author,size,numberOfPages);
      
        try {
            BufferedWriter writer = new BufferedWriter(new FileWriter(filePath, true));
            if(jRadioButton1.isSelected()){
                writer.write(music1 + "\n");
                textArea1.append(music1 + "\n\n");
            }
            if(jRadioButton2.isSelected()){
                writer.write(video1 + "\n");
                textArea1.append(video1 + "\n\n");
            }
            if(jRadioButton3.isSelected()){
                writer.write(ebook1 + "\n");
                textArea1.append(ebook1 + "\n\n");
            }  
            writer.close();
        } catch(IOException e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

